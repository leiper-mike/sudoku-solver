def cellsFromNums(nums:list[list[int]]) -> list[list[Cell]]:
     cells = []
     for i in range(0,9):
          cells.append([])
          for j in range(0,9):
               cells[i].append(Cell((0,0),(0,0)))
               cells[i][j].setNum(nums[i][j])
     return cells
